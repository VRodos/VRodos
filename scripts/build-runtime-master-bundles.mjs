async function buildContract() {
  const raw = await readFile(contractPath, 'utf8');
  const parsed = JSON.parse(raw);
  const source = [
    '/* Generated by scripts/build-runtime-master-bundles.mjs. Do not edit directly. */',
    '(function () {',
    `    window.VRODOS_RUNTIME_SETTINGS_CONTRACT = ${JSON.stringify(parsed, null, 4)};`,
    '}());',
    ''
  ].join('\n');

  await writeFile(contractOutputPath, source, 'utf8');
}

async function readSource(file) {
  const source = await readFile(file, 'utf8');
  const relative = path.relative(rootDir, file).replaceAll(path.sep, '/');
  return `\n/* ===== ${relative} ===== */\n${source.trim()}\n`;
}

async function buildBundle(bundle) {
  const sources = [];
  for (const file of bundle.files) {
    sources.push(await readSource(file));
  }

  const outputPath = path.join(outputDir, bundle.output);
  const header = '/* Generated by scripts/build-runtime-master-bundles.mjs. Do not edit directly. */\n';
  await writeFile(outputPath, header + sources.join('\n'), 'utf8');
  console.log(`Built ${path.relative(rootDir, outputPath)}`);
}

await mkdir(outputDir, { recursive: true });
await buildContract();
console.log(`Built ${path.relative(rootDir, contractOutputPath)}`);

for (const bundle of bundles) {
  await buildBundle(bundle);
}
